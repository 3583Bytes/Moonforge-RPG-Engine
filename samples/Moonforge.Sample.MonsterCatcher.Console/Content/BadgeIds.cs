namespace Moonforge.Sample.MonsterCatcher.Content;

/// <summary>
/// String ids for the eight gym badges. Used as keys in the player's earned-badge set
/// and shown on the HUD. Match the order of <see cref="GymLeaders.All"/>.
/// </summary>
internal static class BadgeIds
{
    public const string Stone     = "badge.stone";      // Gym 1 (Rock)
    public const string Tide      = "badge.tide";       // Gym 2 (Water)
    public const string Whisper   = "badge.whisper";    // Gym 3 (Ghost)
    public const string Spark     = "badge.spark";      // Gym 4 (Electric)
    public const string Quake     = "badge.quake";      // Gym 5 (Ground)
    public const string Sky       = "badge.sky";        // Gym 6 (Flying)
    public const string Ember     = "badge.ember";      // Gym 7 (Fire)
    public const string Shadow    = "badge.shadow";     // Gym 8 (Dark)
}
